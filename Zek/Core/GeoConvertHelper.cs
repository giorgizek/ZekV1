        /// <summary>
        /// კონვერტირება Codes და Direction.
        /// </summary>
        /// <param name="text">ორიგინალი ტექსტი</param>
        /// <param name="fromCodeName">Codes[fromCodeName]</param>
        /// <param name="toCodeName">Codes[toCodeName]</param>
        /// <param name="direction">Direction[]direction</param>
        /// <returns>აბრუნებს დაკონვერტირებულ ტექსტს</returns>
        public static string Convert(string text, string fromCodeName, string toCodeName, string direction)
        {
            var fromCodes = Codes[fromCodeName];
            var toCodes = Codes[toCodeName];

            var directionCodes = (Direction.ContainsKey(direction) ? Direction[direction] : null);

            var sb = new StringBuilder(text);
            for (var i = 0; i < text.Length; i++)
            {
                var index = Array.IndexOf(fromCodes, (int)text[i]);
                if (index == -1) continue;

                if (directionCodes != null) index = directionCodes[index];
                sb[i] = (char)toCodes[index];
            }

            return sb.ToString();
        }
