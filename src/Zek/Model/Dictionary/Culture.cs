using Microsoft.EntityFrameworkCore;
using Zek.Data.Entity;

namespace Zek.Model.Dictionary
{
    public class Culture
    {
        public byte Id { get; set; }
        public string Code { get; set; }
        public string Name { get; set; }
    }

    public class CultureMap : EntityTypeMap<Culture>
    {
        public CultureMap(ModelBuilder builder) : base(builder)
        {
            ToTable("DD_Culture", "Dictionary");
            HasKey(t => t.Id);
            Property(t => t.Id).ValueGeneratedOnAdd();

            Property(t => t.Code).HasMaxLength(10).IsRequired();
            HasIndex(t => t.Code).IsUnique();
            Property(t => t.Name).HasMaxLength(200).IsRequired();
            HasIndex(t => t.Name).IsUnique();
        }
    }
}
